"""
Python logical operators are spelled out:
    and
    or
    not (inverse operator that can have same functional effect has ![variable] in JS)
"""
print(not 3 < 10)
print(10 == 10)
print(10 >= 11)
print((10 < 100 and 10 < 5) or 9 % 3 == 0)
